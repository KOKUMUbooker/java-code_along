public class WrapperClasses {
    public static void main(String[] args) {
        // Using wrapper classes over their corresponding primitive forms allows access
        // to additional methods from the object

        /*
         * Autoboxing - automatic conversion of primitive data types to their
         * corresponding object wrapper class;
         * eg
         * /Primitive/ /Wrapper/
         * int Integer
         * boolean Boolean
         * char Character
         * double Double
         */

        /*
         * Unboxing - Automatic conversion of wrapper class to primitive
         * - it entails using a wrapper object as thought its in its primitive form
         */

        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "Doe";

    }
}
